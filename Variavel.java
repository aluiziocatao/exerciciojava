public class Variavel { 
  /* Método principal da class */
  public static void main(String[] args) {
    String nome = "Aluízio";
    int idade = 31;
    boolean casado = true;
    // Imprime em tela o resultado da nossa variável
    System.out.println(nome);
    System.out.println(idade);
    System.out.println(casado);
  }// fim do método
}// fim da classe
